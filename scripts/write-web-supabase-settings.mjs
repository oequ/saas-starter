#!/usr/bin/env node
/** Writes apps/web + api-console supabase.settings.ts for local Supabase / CI. */
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';

function readAnonKeyFromSupabaseStatus() {
  try {
    const out = execSync('npx supabase status -o json', {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    });
    const json = JSON.parse(out);
    return json.ANON_KEY ?? json.anon_key ?? json.API_ANON_KEY ?? null;
  } catch {
    return null;
  }
}

const url = process.env['SUPABASE_URL'] ?? 'http://127.0.0.1:54321';
const anonKey =
  process.env['SUPABASE_ANON_KEY'] ??
  readAnonKeyFromSupabaseStatus() ??
  'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZS1kZW1vIiwicm9sZSI6ImFub24iLCJleHAiOjE5ODM4MTI5OTZ9.CRXP1A7WOeoJeXxjNni43kdQwgnWNReilDMblYTn_I0';
const stripeEnabled =
  process.env['STRIPE_ENABLED'] === 'true' ||
  process.env['STRIPE_ENABLED'] === '1';
const billingProviderEnv = process.env['BILLING_PROVIDER']?.trim();
const billingProvider = billingProviderEnv
  ? `'${billingProviderEnv.replace(/'/g, "\\'")}'`
  : stripeEnabled
    ? "'stripe'"
    : "'mock'";

const settingsBody = `  url: '${url}',
  anonKey: '${anonKey}',
  billingProvider: ${billingProvider} as const,
  stripeEnabled: ${stripeEnabled},
  requireEmailConfirmation: false,
`;

const targets = [
  {
    path: join(process.cwd(), 'apps/web/src/app/supabase.settings.ts'),
    exportName: 'webSupabaseSettings',
  },
  {
    path: join(process.cwd(), 'apps/api-console/src/app/supabase.settings.ts'),
    exportName: 'apiConsoleSupabaseSettings',
  },
];

for (const { path: target, exportName } of targets) {
  const contents = `/** Generated by scripts/write-web-supabase-settings.mjs — do not commit secrets in prod. */
export const ${exportName} = {
${settingsBody}};
`;
  writeFileSync(target, contents, 'utf8');
  console.log(`Wrote ${target}`);
}
